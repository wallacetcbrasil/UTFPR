# Crie um programa que calcule a area de um triangulo cujas dimensões de base e da altura são dadas.
# A saida do programa deverá ser exatamente igual a saida do programa

print('Bem vindo a calculadora da area de um triangulo')
height = int(input('Digite uma altura: '))
base = int(input('Digite uma base: '))
area = base * height/2
print('A area de um triangulo com base', base,'e altura', height, 'é', area)